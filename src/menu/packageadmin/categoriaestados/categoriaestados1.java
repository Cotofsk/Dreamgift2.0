    private void btnActualizarCatEstadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarCatEstadosActionPerformed

        Connection con = null;

        try {

            con = getConection();

            ps = con.prepareStatement("UPDATE estados SET descripccion_estado=?, codigoestado=?, estado=? WHERE idEstados=?");

            ps.setString(1, txtNombreCatEstados.getText());
            ps.setString(2, txtCodigoCatEstados.getText());
            ps.setString(3, cbxCatEstados.getSelectedItem().toString());
            ps.setString(4, txtidCatEstados.getText());

            int res = ps.executeUpdate();

            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Red social modificada");
                limpiarCajasCatEstados();
                ActualizarAutomaticamenteCatEstados();
                dispose ();

            } else {
                JOptionPane.showMessageDialog(null, "Error al modificar la red social");

            }

            con.close();

        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_btnActualizarCatEstadosActionPerformed

