    private void btnGuardarCatEstadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCatEstadosActionPerformed
        Connection con = null;

        try {

            con = getConection();
            ps = con.prepareStatement("INSERT INTO estados (descripccion_estado,codigoestado,estado) VALUES(?,?,?)");
            ps.setString(1, txtNombreCatEstados.getText());
            ps.setString(2, txtCodigoCatEstados.getText());
            ps.setString(3, cbxCatEstados.getSelectedItem().toString());

            int res = ps.executeUpdate();

            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Variable de estado de venta guardada exitosamente");

                limpiarCajasCatEstados();
                ActualizarAutomaticamenteCatEstados();
                dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar la nueva variable de estado de venta");
                limpiarCajasCatEstados();
                dispose();
            }

            con.close();

        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_btnGuardarCatEstadosActionPerformed

