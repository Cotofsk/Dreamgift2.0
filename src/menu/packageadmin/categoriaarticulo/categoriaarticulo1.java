    private void btnGuardarCatArticuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCatArticuloActionPerformed
        Connection con = null;

        try {

            con = getConection();
            ps = con.prepareStatement("INSERT INTO categorias (catarticulo,codigo,estado) VALUES(?,?,?)");
            ps.setString(1, txtNombreCatArticulo.getText());
            ps.setString(2, txtCodigoCatArticulo.getText());
            ps.setString(3, cbxEstadoCatArticulo.getSelectedItem().toString());

            int res = ps.executeUpdate();

            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Categoria de articulo guardada correctamente");

                limpiarCajasCatArticulo();
                ActualizarAutomaticamenteCatArticulo();
                dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar nueva categoria de articulo");
                limpiarCajasCatArticulo();
                dispose();
            }

            con.close();

        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_btnGuardarCatArticuloActionPerformed

