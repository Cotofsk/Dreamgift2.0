    private void btnActualizarCatArticuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarCatArticuloActionPerformed

        Connection con = null;

        try {

            con = getConection();

            ps = con.prepareStatement("UPDATE categorias SET catarticulo=?, codigo=?, estado=? WHERE id_categoria=?");

            ps.setString(1, txtNombreCatArticulo.getText());
            ps.setString(2, txtCodigoCatArticulo.getText());
            ps.setString(3, cbxEstadoCatArticulo.getSelectedItem().toString());
            ps.setString(4, txtidCatArticulo.getText());

            int res = ps.executeUpdate();

            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Categoria de articulo modificada");
                limpiarCajasCatArticulo();
                ActualizarAutomaticamenteCatArticulo();
                dispose ();

            } else {
                JOptionPane.showMessageDialog(null, "Error al modificar la categoria de articulo");

            }

            con.close();

        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_btnActualizarCatArticuloActionPerformed

