    private void btnActualizarPacksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarPacksActionPerformed

        Connection con = null;

        try {

            con = getConection();

            ps = con.prepareStatement("UPDATE packs SET nombre=?, codigo=?, estado=?, precio=?, compuesto=?, WHERE idpacks=?");

            ps.setString(1, txtNombrePacks.getText());
            ps.setString(2, txtCodigoPacks.getText());
            ps.setString(3, cbxEstado.getSelectedItem().toString());
            ps.setString(4, txtid.getText());

            int res = ps.executeUpdate();

            if (res > 0) {
                JOptionPane.showMessageDialog(null, "El Pack ha sido modificado");
                limpiarCajaspacks();
                ActualizarAutomaticamentePacks();
                dispose ();

            } else {
                JOptionPane.showMessageDialog(null, "Error al modificar Pack");

            }

            con.close();

        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_btnActualizarPacksActionPerformed

