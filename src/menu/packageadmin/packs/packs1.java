    private void btnGuardarPacksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarPacksActionPerformed
        Connection con = null;

        try {

            con = getConection();
            ps = con.prepareStatement("INSERT INTO packs (nombre,codigo,precio,estado) VALUES(?,?,?,?)");
            ps.setString(1, txtNombrePacks.getText());
            ps.setString(2, txtCodigoPacks.getText());
             ps.setString(3, txtPrecio.getText());
            ps.setString(4, cbxEstado.getSelectedItem().toString());

            int res = ps.executeUpdate();

            if (res > 0) {
                JOptionPane.showMessageDialog(null, "El Pack ha sido guardado correctamente");

                limpiarCajasPacks();
                ActualizarAutomaticamente();
                dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar nueva Pack");
                limpiarCajasPacks();
                dispose();
            }

            con.close();

        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_btnGuardarPacksActionPerformed

