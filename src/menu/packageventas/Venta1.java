    private void btnGuardarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarClienteActionPerformed

        Connection con = null;

        try {

            con = getConection();
            ps = con.prepareStatement("INSERT INTO clientes (rut,nombre,direccion,fono,fecha_nac,email ) VALUES(?,?,?,?,?,?)");
            ps.setString(1, txtRutCliente.getText());
            ps.setString(2, txtNombreCliente.getText());
            ps.setString(3, txtDireccionCliente.getText());
            ps.setString(4, txtTelefonoCliente.getText());
            ps.setDate(5, new java.sql.Date(jDNacimiento.getDate().getTime()));
            ps.setString(6, txtEmailCliente.getText());
            

            int res = ps.executeUpdate();

            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Cliente Guardado Exitosamente");
               // limpiarCajasCliente();
                ActualizarAutomaticamenteCliente();
            } else {
                JOptionPane.showMessageDialog(null, "Error al Guardar Cliente");
                //limpiarCajasCliente();
            }

            con.close();

        } catch (Exception e) {
            System.err.println(e);
        }

        
        
        
        
        
    }//GEN-LAST:event_btnGuardarClienteActionPerformed

